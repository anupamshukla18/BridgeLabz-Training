class KilometersToMiles{
	public static void main(String[] args){
		//Initializing the distance in kilometers
		double kilometers = 10.8;
		
		//Calculating the result
		double result = kilometers * 10.6;
		
		//Displaying the result
		System.out.println("The distance " + kilometers + " km in miles is " + result);
	}
}
		