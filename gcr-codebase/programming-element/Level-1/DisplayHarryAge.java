class DisplayHarryAge {
	public static void main(String[] args){
		//person's name
		String name = "Harry";
		
		//current year
		int currentYear = 2024;
		
		//person's birthYear
		int birthYear = 2000;
		
		//Calculating the age
		int age = currentYear - birthYear;
		
		//Displaying the result
		System.out.println(name + "'s age in "+ currentYear + " is " + age);
	}
}	